#!/usr/bin/env python3
"""
SASA Analysis Script: Filtration and Analysis Logic

## Overview

Performs **Solvent Accessible Surface Area (SASA)** analysis on antibody-antigen complexes to quantify binding effects on surface accessibility.

## Data Sources

- **PDB structures**: From the SAbDab (Structural Antibody Database) local mirror, renumbered by the ANARCI/OPiG Chothia scheme.
- **Chain identifications (Hchain, Lchain, antigen)**: From SAbDab bulk download (`data/sabdab/sabdab_summary_all_2024-01-26.tsv`).
- **V/J gene annotations and organism**: From ANARCI (profile HMM-based germline assignment), stored in `data/sabdab/sabdab_anarci_annotations.tsv`. Generated by `annotate_sabdab_anarci.py`.

## Filtering Pipeline (6 Stages)

### Stage 1: File Discovery
Scans PDB directory for `*.pdb` files.

### Stage 2: Organism Filter (optional)
Filters by species if `--organism` specified (exact match, case-insensitive). Cheapest and most selective filter.

### Stage 3: Chain Completeness Filter
Requires heavy + light + antigen chain(s) for complete analysis. Fast metadata check.

### Stage 4: Heavy-Chain-Only Antibody Filter
Excludes PDBs where the "light chain" is actually a heavy chain (HCAbs, nanobodies, VHH fragments). Detected by ANARCI assigning an IGH* V gene to the light chain.

### Stage 5: V/J Gene Consistency Filter
Ensures identical V/J genes (`va`, `ja`, `vb`, `jb`) within each PDB to remove pdbs with multiple different antibodies.

### Stage 6: File Limit
Applies `--max-files` limit if specified.

## SASA/RSA Calculation

For each PDB, computes absolute SASA (Å²) using DSSP and normalizes to Relative Solvent Accessibility
(RSA, 0-1 scale) using Wilke reference maximum values per amino acid type. Both SASA and RSA are
reported for every residue in every scenario.

### Six structural scenarios

SASA/RSA is calculated six times per structure, progressively stripping binding partners to isolate
each partner's contribution to surface burial:

| Scenario | Chains present | Purpose |
|----------|---------------|---------|
| 1. `heavy_light_antigen` | H + L + Ag | Full bound complex |
| 2. `heavy_light` | H + L | Antibody without antigen |
| 3. `heavy_only` | H | Heavy chain alone |
| 4. `heavy_no_vdj_junction` | H minus sites 95-102 | Without V/D/J junction |
| 5. `heavy_no_fulldj` | H minus sites 95-113 | Without full DJ region |
| 6. `heavy_no_cdr3` | H minus sites 96-101 | Without CDR3 (Chothia definition) |

### Burial effects (derived from scenario differences)

Each effect measures how much a binding partner buries each residue (negative = burial):

```
antigen_effect  = scenario 1 - scenario 2  (burial by antigen)
light_effect    = scenario 2 - scenario 3  (burial by light chain)
vdj_junction_effect = scenario 3 - scenario 4  (burial by CDR-H3/VDJ junction)
fulldj_effect   = scenario 3 - scenario 5  (burial by full DJ region)
cdr3_effect     = scenario 3 - scenario 6  (burial by CDR3)
```

Both absolute (SASA) and relative (RSA) effects are reported, along with relative-to-unbound
ratios (e.g., `rsa_antigen_relative = effect / rsa_heavy_light`).

## Backbone Angles

Phi (φ) and psi (ψ) dihedral angles are calculated once from the original PDB coordinates using
BioPython's PPBuilder. These are intrinsic to the structure and identical across all scenarios.

## Output

Per-residue CSV with: structure metadata, V/J gene annotations, SASA and RSA for all six scenarios,
burial effects (absolute, relative, and ratio), phi/psi angles, and Cα coordinates.

## Usage

    python run_sasa_analysis.py --help
    python run_sasa_analysis.py --scheme opig-chothia --output _output/sasa_human_chothia_anarci.csv --organism "homo sapiens"
"""

