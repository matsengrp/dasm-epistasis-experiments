class ChainSelector(Select):
    """Select only specific chains from PDB structure."""

    def __init__(self, keep_chains):
        self.keep_chains = (
            keep_chains if isinstance(keep_chains, list) else [keep_chains]
        )

    def accept_chain(self, chain):
        return chain.id in self.keep_chains


class HeavyChainRangeRemover(Select):
    """Remove a range of Chothia sites from the heavy chain, keep everything else.

    Used to measure how specific heavy chain regions bury other sites.
    The range is inclusive on both ends.
    """

    def __init__(self, heavy_chain_id, start_site, end_site):
        self.heavy_chain_id = heavy_chain_id
        self.start_site = start_site
        self.end_site = end_site

    def accept_residue(self, residue):
        chain_id = residue.get_parent().id
        if chain_id == self.heavy_chain_id:
            site_num = residue.id[1]
            return site_num < self.start_site or site_num > self.end_site
        return True


# Scenarios that remove a range of heavy chain sites.
# Each maps to (start_site, end_site) inclusive.
HEAVY_REMOVAL_SCENARIOS = {
    "heavy_no_vdj_junction": (95, 102),  # Variable D/J junction
    "heavy_no_fulldj": (95, 113),        # Full DJ region including FR4
    "heavy_no_cdr3": (96, 101),          # CDR3 Chothia definition
}


class StructureFilter:
    """Unified filtering pipeline for PDB structures."""

    def __init__(self, metadata_extractor, verbose=False):
        self.metadata = metadata_extractor
        self.verbose = verbose
        self.filter_stats = {}

    def apply_filters(self, pdb_directory, organism_filter=None, max_files=None):
        """Apply all filters in sequence with consolidated reporting."""

        # Stage 1: Get all available PDB files
        all_files = list(Path(pdb_directory).glob("*.pdb"))
        self._record_stage("available_files", len(all_files), all_files)

        if not self.metadata:
            # No metadata = no filtering possible
            final_files = all_files[:max_files] if max_files else all_files
            self._record_stage(
                "final_files",
                len(final_files),
                final_files,
                note="No metadata filtering",
            )
            self._print_filter_summary()
            return final_files

        # Stage 2: Organism filter (if specified) - cheapest, most selective
        if organism_filter:
            organism_ids = self._get_organism_filtered_ids(organism_filter)
            files_after_organism = self._filter_by_pdb_ids(all_files, organism_ids)
            self._record_stage(
                "organism_filtered",
                len(files_after_organism),
                files_after_organism,
                note=f"organism='{organism_filter}'"
            )
        else:
            files_after_organism = all_files
            self._record_stage(
                "organism_filtered",
                len(files_after_organism),
                files_after_organism,
                note="no organism filter"
            )

        # Stage 3: Chain completeness filter - cheap metadata check
        files_with_complete_chains = self._filter_by_complete_chains(
            files_after_organism
        )
        self._record_stage(
            "complete_chains",
            len(files_with_complete_chains),
            files_with_complete_chains,
        )

        # Stage 4: Heavy-chain-only antibody filter - exclude PDBs where
        # the "light chain" is actually a heavy chain (nanobodies, HCAbs, VHH)
        hcab_ids = self.metadata.get_heavy_chain_only_antibody_pdbs()
        files_no_hcabs = [f for f in files_with_complete_chains
                          if f.stem.upper() not in {pid.upper() for pid in hcab_ids}]
        self._record_stage(
            "heavy_chain_only_filtered",
            len(files_no_hcabs),
            files_no_hcabs,
            note=f"{len(hcab_ids)} heavy-chain-only antibody PDBs excluded"
        )

        # Stage 5: V/J consistency filter - more expensive groupby operations
        consistent_ids, kept, removed = self.metadata.get_pdbs_with_consistent_vj_genes()
        files_with_consistent_vj = self._filter_by_pdb_ids(
            files_no_hcabs, consistent_ids
        )
        self._record_stage(
            "vj_consistent",
            len(files_with_consistent_vj),
            files_with_consistent_vj,
            note=f"{kept} kept, {removed} removed"
        )

        # Stage 6: Apply max_files limit
        if max_files:
            final_files = files_with_consistent_vj[:max_files]
            self._record_stage("max_files_limited", len(final_files), final_files)
        else:
            final_files = files_with_consistent_vj

        self._print_filter_summary()
        return final_files

    def _get_organism_filtered_ids(self, organism_filter):
        """Get PDB IDs matching organism filter (exact match, case-insensitive)."""
        if not self.metadata.abid_df.empty:
            filtered_df = self.metadata.abid_df[
                self.metadata.abid_df["organism"].str.lower() == organism_filter.lower()
            ]
            return set(filtered_df["pdbid"].unique())
        return set()

    def _filter_by_pdb_ids(self, pdb_files, valid_ids):
        """Keep only files with PDB IDs in the valid set."""
        valid_ids_upper = {str(pid).upper() for pid in valid_ids}
        return [f for f in pdb_files if f.stem.upper() in valid_ids_upper]

    def _filter_by_complete_chains(self, pdb_files):
        """Keep only files with heavy, light, and antigen chain information."""
        valid_files = []
        rejected_count = {"no_heavy": 0, "no_light": 0, "no_antigen": 0}

        for pdb_file in pdb_files:
            pdb_id = pdb_file.stem
            chain_info = self.metadata.get_chain_info(pdb_id)

            has_heavy = bool(chain_info.get("heavy_chain"))
            has_light = bool(chain_info.get("light_chain"))
            has_antigen = bool(chain_info.get("antigen_chains"))

            if has_heavy and has_light and has_antigen:
                valid_files.append(pdb_file)
            else:
                # Track rejection reasons
                if not has_heavy:
                    rejected_count["no_heavy"] += 1
                if not has_light:
                    rejected_count["no_light"] += 1
                if not has_antigen:
                    rejected_count["no_antigen"] += 1

                if self.verbose:
                    missing = []
                    if not has_heavy:
                        missing.append("heavy")
                    if not has_light:
                        missing.append("light")
                    if not has_antigen:
                        missing.append("antigen")
                    print(f"Skipping {pdb_id}: missing {', '.join(missing)} chain(s)")

        if self.verbose and any(rejected_count.values()):
            total_rejected = len(pdb_files) - len(valid_files)
            print(
                f"Chain filtering: {len(valid_files)} kept, {total_rejected} rejected"
            )
            for reason, count in rejected_count.items():
                if count > 0:
                    print(f"  {reason}: {count} files")

        return valid_files

    def _record_stage(self, stage_name, count, files, note=None):
        """Record statistics for a filtering stage."""
        self.filter_stats[stage_name] = {"count": count, "files": files, "note": note}

    def _print_filter_summary(self):
        """Print consolidated filtering summary."""
        if not self.verbose:
            return

        print("\n=== Filtering Pipeline Summary ===")
        prev_count = None
        for stage, data in self.filter_stats.items():
            count = data["count"]
            note = f" ({data['note']})" if data.get("note") else ""

            # Calculate how many were dropped at this stage
            if prev_count is not None:
                dropped = prev_count - count
                drop_pct = (dropped / prev_count * 100) if prev_count > 0 else 0
                drop_info = f", dropped {dropped} ({drop_pct:.1f}%)"
            else:
                drop_info = ""

            print(f"{stage.replace('_', ' ').title()}: {count} files{note}{drop_info}")
            prev_count = count

        # Show sample of final files
        if self.filter_stats:
            final_stage = list(self.filter_stats.keys())[-1]
            final_files = self.filter_stats[final_stage]["files"][:5]
            if final_files:
                print(f"Sample final files: {[f.stem for f in final_files]}")


class ProteinMetadataExtractor:
    """Extract protein metadata from SAbDab summary tables."""

    def __init__(self, verbose=False):
        self.abid_info_path = SABDAB_ABID_INFO_PATH
        self.chain_info_path = SABDAB_CHAIN_INFO_PATH
        self.abid_df = None
        self.chain_df = None
        self.verbose = verbose
        self.pdb_lookup = {}
        self.load_sabdab_data()

    def load_sabdab_data(self):
        """Load the SAbDab summary tables with V/D/J gene and chain information."""
        # Load abid_info table (V/D/J genes, organism)
        if os.path.exists(self.abid_info_path):
            self.abid_df = pd.read_table(self.abid_info_path)
            # Select relevant columns: pdbid, organism, va, ja, vb, jb, chainseq_a, chainseq_b
            required_cols = [
                "pdbid",
                "organism",
                "va",
                "ja",
                "vb",
                "jb",
                "chainseq_a",
                "chainseq_b",
            ]
            available_cols = [
                col for col in required_cols if col in self.abid_df.columns
            ]
            self.abid_df = self.abid_df[available_cols]
            if self.verbose:
                print(f"Loaded abid info with {len(self.abid_df)} entries")
        else:
            if self.verbose:
                print(f"Warning: Abid info file not found at {self.abid_info_path}")
            self.abid_df = pd.DataFrame()

        # Load chain_info table (chain identifications)
        if os.path.exists(self.chain_info_path):
            self.chain_df = pd.read_table(self.chain_info_path)
            # Select relevant columns: pdb, Hchain, Lchain, antigen_chain
            required_cols = ["pdb", "Hchain", "Lchain", "antigen_chain"]
            available_cols = [
                col for col in required_cols if col in self.chain_df.columns
            ]
            self.chain_df = self.chain_df[available_cols]
            if self.verbose:
                print(f"Loaded chain info with {len(self.chain_df)} entries")
        else:
            if self.verbose:
                print(f"Warning: Chain info file not found at {self.chain_info_path}")
            self.chain_df = pd.DataFrame()

        # Create lookup dictionary for faster access
        self.pdb_lookup = {}

        # Process abid_df
        if not self.abid_df.empty:
            for _, row in self.abid_df.iterrows():
                pdb_id = str(row["pdbid"]).strip()
                if pdb_id not in self.pdb_lookup:
                    self.pdb_lookup[pdb_id] = {}
                self.pdb_lookup[pdb_id].update(row.to_dict())
                # Also add case variations
                self.pdb_lookup[pdb_id.upper()] = self.pdb_lookup[pdb_id]
                self.pdb_lookup[pdb_id.lower()] = self.pdb_lookup[pdb_id]

        # Process chain_df
        if not self.chain_df.empty:
            for _, row in self.chain_df.iterrows():
                pdb_id = str(row["pdb"]).strip()
                if pdb_id not in self.pdb_lookup:
                    self.pdb_lookup[pdb_id] = {}
                self.pdb_lookup[pdb_id].update(row.to_dict())
                # Also add case variations
                self.pdb_lookup[pdb_id.upper()] = self.pdb_lookup[pdb_id]
                self.pdb_lookup[pdb_id.lower()] = self.pdb_lookup[pdb_id]

    def get_protein_metadata(self, pdb_id):
        """Get protein metadata for a given PDB ID."""
        if not self.pdb_lookup:
            return {"pdb_id": pdb_id}

        # Try to find the entry in our lookup dictionary
        pdb_id_clean = str(pdb_id).strip()
        entry = None

        # Try different case variations
        for key in [pdb_id_clean.upper(), pdb_id_clean.lower(), pdb_id_clean]:
            if key in self.pdb_lookup:
                entry = self.pdb_lookup[key]
                break

        if entry is None:
            return {"pdb_id": pdb_id}

        metadata = {
            "pdb_id": pdb_id,
            "organism": entry.get("organism", ""),
            "v_gene_light": entry.get("va", ""),  # Light chain V gene
            "j_gene_light": entry.get("ja", ""),  # Light chain J gene
            "v_gene_heavy": entry.get("vb", ""),  # Heavy chain V gene
            "j_gene_heavy": entry.get("jb", ""),  # Heavy chain J gene
            "heavy_chain_id": entry.get("Hchain", ""),
            "light_chain_id": entry.get("Lchain", ""),
            "antigen_chains": entry.get("antigen_chain", ""),
        }

        return metadata

    def get_heavy_chain_only_antibody_pdbs(self):
        """Get PDB IDs where the "light chain" is actually a heavy chain.

        This catches heavy-chain-only antibodies (HCAbs), nanobodies, and VHH
        fragments, where SAbDab lists an Lchain that is a second copy of the
        heavy chain. Detected by ANARCI assigning an IGH* V gene to the light
        chain (va column).
        """
        if self.abid_df is None or len(self.abid_df) == 0:
            return set()

        hcab_ids = set()
        for pdb_id, group in self.abid_df.groupby("pdbid"):
            va_values = group["va"].dropna() if "va" in group.columns else pd.Series()
            if any(str(v).startswith("IGH") for v in va_values):
                hcab_ids.add(pdb_id)

        if self.verbose:
            print(f"Heavy-chain-only antibody filter: {len(hcab_ids)} PDBs identified")

        return hcab_ids

    def get_pdbs_with_consistent_vj_genes(self):
        """Get PDB IDs where all entries have the same V and J genes."""
        if self.abid_df is None or len(self.abid_df) == 0:
            return set(), 0, 0

        # Group by PDB ID and check for consistency
        consistent_pdbs = set()
        total_pdbs = self.abid_df["pdbid"].nunique()

        for pdb_id, group in self.abid_df.groupby("pdbid"):
            # Check if all V and J genes are the same within this PDB
            va_unique = group["va"].dropna().unique() if "va" in group.columns else []
            ja_unique = group["ja"].dropna().unique() if "ja" in group.columns else []
            vb_unique = group["vb"].dropna().unique() if "vb" in group.columns else []
            jb_unique = group["jb"].dropna().unique() if "jb" in group.columns else []

            # PDB is consistent if each gene type has at most one unique value
            is_consistent = (
                len(va_unique) <= 1
                and len(ja_unique) <= 1
                and len(vb_unique) <= 1
                and len(jb_unique) <= 1
            )

            if is_consistent:
                consistent_pdbs.add(pdb_id)

        removed_count = total_pdbs - len(consistent_pdbs)

        if self.verbose:
            print(
                f"V/J gene consistency filter: {len(consistent_pdbs)} PDBs kept, {removed_count} PDBs removed (out of {total_pdbs} total)"
            )

        return consistent_pdbs, len(consistent_pdbs), removed_count

    def get_chain_info(self, pdb_id):
        """Get chain identification information for a PDB ID."""
        metadata = self.get_protein_metadata(pdb_id)

        # Parse antigen chains (can be multiple, separated by ' | ')
        antigen_chain_str = metadata.get("antigen_chains", "")
        antigen_chains = []
        if antigen_chain_str and pd.notna(antigen_chain_str):
            antigen_chains = [
                chain.strip() for chain in str(antigen_chain_str).split("|")
            ]

        # Get antibody chains
        antibody_chains = []
        if metadata.get("heavy_chain_id"):
            antibody_chains.append(metadata["heavy_chain_id"])
        if metadata.get("light_chain_id"):
            antibody_chains.append(metadata["light_chain_id"])

        return {
            "antibody_chains": antibody_chains,
            "antigen_chains": antigen_chains,
            "heavy_chain": metadata.get("heavy_chain_id", ""),
            "light_chain": metadata.get("light_chain_id", ""),
        }


class SASACalculator:
    """Calculate SASA and RSA for PDB structures with protein metadata integration."""

    def __init__(self, temp_dir="_temp_sasa", metadata_extractor=None, numbering_scheme=None):
        import os

        # Make temp directory unique per process to avoid conflicts
        unique_temp_dir = f"{temp_dir}_{os.getpid()}"
        self.temp_dir = Path(unique_temp_dir)
        self.temp_dir.mkdir(exist_ok=True)
        self.metadata_extractor = metadata_extractor
        self.numbering_scheme = numbering_scheme

        # Wilke reference values for calculating absolute ASA from relative ASA
        self.wilke_reference = {
            "A": 129.0,
            "R": 274.0,
            "N": 195.0,
            "D": 193.0,
            "C": 167.0,
            "Q": 225.0,
            "E": 223.0,
            "G": 104.0,
            "H": 224.0,
            "I": 197.0,
            "L": 201.0,
            "K": 236.0,
            "M": 224.0,
            "F": 240.0,
            "P": 159.0,
            "S": 155.0,
            "T": 172.0,
            "W": 285.0,
            "Y": 263.0,
            "V": 174.0,
        }

    def _format_site_label(self, residue_number, insertion_code):
        """
        Create a standardized site label from residue number and insertion code.

        For Chothia: "81A" stays as "81A"
        For IMGT: "112A" becomes "112.1", "112B" becomes "112.2", etc.
        No insertion: "100" stays as "100" (string, not "100.0")

        Parameters:
        -----------
        residue_number : int
            The residue number
        insertion_code : str
            The insertion code (single letter or empty string)

        Returns:
        --------
        str
            Formatted site label (always string, never float)
        """
        # Convert residue_number to int first to ensure no decimal points
        residue_num_int = int(residue_number)

        if not insertion_code:
            # Return as string without decimal point (e.g., "100" not "100.0")
            return str(residue_num_int)

        # For IMGT scheme, convert A->1, B->2, C->3, etc.
        if self.numbering_scheme and "imgt" in self.numbering_scheme.lower():
            # Convert insertion code letter to number (A=1, B=2, C=3, ...)
            insertion_num = ord(insertion_code.upper()) - ord('A') + 1
            return f"{residue_num_int}.{insertion_num}"
        else:
            # For Chothia and other schemes, keep as is (e.g., "81A")
            return f"{residue_num_int}{insertion_code}"

    def _create_nan_result(self, pdb_id, scenario, metadata):
        """Create a DataFrame with NaN values when SASA calculation cannot be performed."""
        return pd.DataFrame(
            [
                {
                    "chain_id": np.nan,
                    "residue_number": np.nan,
                    "insertion_code": np.nan,
                    "site": np.nan,
                    "amino_acid": np.nan,
                    "sasa": np.nan,
                    "rsa": np.nan,
                    "ca_coordinates": np.nan,
                    "scenario": scenario,
                    # Add protein metadata
                    "pdb_id": pdb_id,
                    "organism": metadata.get("organism", ""),
                    "v_gene_light": metadata.get("v_gene_light", ""),
                    "j_gene_light": metadata.get("j_gene_light", ""),
                    "v_gene_heavy": metadata.get("v_gene_heavy", ""),
                    "j_gene_heavy": metadata.get("j_gene_heavy", ""),
                    "heavy_chain_id": metadata.get("heavy_chain_id", ""),
                    "light_chain_id": metadata.get("light_chain_id", ""),
                    "antigen_chains": metadata.get("antigen_chains", ""),
                }
            ]
        )

    def _calculate_sasa(self, pdb_path, scenario="heavy_light_antigen"):
        """Internal method to calculate SASA with protein metadata.

        Parameters:
        -----------
        scenario : str
            One of: "heavy_light_antigen", "heavy_light", "heavy_only",
            or any key from HEAVY_REMOVAL_SCENARIOS
        """
        parser = PDBParser(PERMISSIVE=True, QUIET=True)
        structure = parser.get_structure("pdb", pdb_path)

        # Extract PDB ID and get metadata
        pdb_id = Path(pdb_path).stem
        metadata = {}
        chain_info = {}
        if self.metadata_extractor:
            metadata = self.metadata_extractor.get_protein_metadata(pdb_id)
            chain_info = self.metadata_extractor.get_chain_info(pdb_id)

            # Always use automatic chain detection from metadata

        # Determine which chains to keep based on scenario
        heavy_chain = chain_info.get("heavy_chain", "")
        light_chain = chain_info.get("light_chain", "")
        antigen_chains = chain_info.get("antigen_chains", [])

        # Determine which chains to keep based on scenario
        if scenario == "heavy_light_antigen":
            keep_chains = [heavy_chain, light_chain] + antigen_chains
            target_chains = [heavy_chain, light_chain]  # Only analyze antibody residues
        elif scenario == "heavy_light":
            keep_chains = [heavy_chain, light_chain]
            target_chains = [heavy_chain, light_chain]
        elif scenario == "heavy_only":
            keep_chains = [heavy_chain]
            target_chains = [heavy_chain]
        elif scenario in HEAVY_REMOVAL_SCENARIOS:
            keep_chains = [heavy_chain]
            target_chains = [heavy_chain]
        else:
            raise ValueError(f"Invalid scenario: {scenario}")

        # Remove empty chain IDs
        keep_chains = [c for c in keep_chains if c]
        target_chains = [c for c in target_chains if c]

        if not target_chains:
            print(
                f"Warning: No valid target chains for {pdb_id} in scenario {scenario}"
            )
            return self._create_nan_result(pdb_id, scenario, metadata)

        # Create temporary PDB file
        temp_id = random.randint(10000, 99999)
        temp_pdb_path = self.temp_dir / f"temp_{temp_id}.pdb"

        try:
            io = PDBIO()
            io.set_structure(structure)

            # Use strict chain filtering - keep only the chains specified for this scenario
            io.save(str(temp_pdb_path), ChainSelector(keep_chains))

            # For heavy chain removal scenarios, further filter to remove site range
            if scenario in HEAVY_REMOVAL_SCENARIOS:
                start_site, end_site = HEAVY_REMOVAL_SCENARIOS[scenario]
                step1_structure = parser.get_structure("step1", str(temp_pdb_path))
                io.set_structure(step1_structure)
                io.save(
                    str(temp_pdb_path),
                    HeavyChainRangeRemover(heavy_chain, start_site, end_site),
                )

            # Reload the filtered structure
            filtered_structure = parser.get_structure("filtered", str(temp_pdb_path))
            filtered_model = filtered_structure[0]

            # Calculate DSSP with warning suppression
            import warnings

            with warnings.catch_warnings():
                warnings.filterwarnings("ignore", message="parse error at line 1")
                dssp = DSSP(
                    filtered_model, str(temp_pdb_path), dssp="mkdssp", acc_array="Wilke"
                )

            # Extract data for antibody chains only
            sasa_data = []

            for key in dssp.keys():
                chain_id, res_id = key

                # Only process target chains (antibody chains for analysis)
                if chain_id not in target_chains:
                    continue

                # Extract residue information
                residue_num = res_id[1]
                insertion_code = res_id[2].strip()

                # Site range filtering removed - now analyzes all residues

                # Get DSSP data
                dssp_data = dssp[key]
                aa = dssp_data[1]  # Amino acid
                rel_asa = dssp_data[
                    3
                ]  # Relative accessible surface area (Wilke normalized, 0-1)

                # Calculate absolute ASA from relative ASA
                max_asa = self.wilke_reference[
                    aa
                ]  # Will raise KeyError for unknown amino acids
                abs_asa = rel_asa * max_asa

                # Get CA coordinates if available
                ca_coord = None
                try:
                    residue = filtered_model[chain_id][res_id]
                    if "CA" in residue:
                        ca_coord = tuple(residue["CA"].coord)
                except:
                    pass

                # Create formatted site label
                site = self._format_site_label(residue_num, insertion_code)

                # Build result row with both SASA data and metadata
                row_data = {
                    "chain_id": chain_id,
                    "residue_number": residue_num,
                    "insertion_code": insertion_code,
                    "site": site,
                    "amino_acid": aa,
                    "sasa": abs_asa,  # Absolute ASA in Å²
                    "rsa": rel_asa,  # Relative ASA (0-1 scale)
                    "ca_coordinates": ca_coord,
                    "scenario": scenario,
                    # Add protein metadata
                    "pdb_id": pdb_id,
                    "organism": metadata.get("organism", ""),
                    "v_gene_light": metadata.get("v_gene_light", ""),
                    "j_gene_light": metadata.get("j_gene_light", ""),
                    "v_gene_heavy": metadata.get("v_gene_heavy", ""),
                    "j_gene_heavy": metadata.get("j_gene_heavy", ""),
                    "heavy_chain_id": metadata.get("heavy_chain_id", ""),
                    "light_chain_id": metadata.get("light_chain_id", ""),
                    "antigen_chains": metadata.get("antigen_chains", ""),
                }

                sasa_data.append(row_data)

            df = pd.DataFrame(sasa_data)
            # Ensure site column is always string type
            if not df.empty and 'site' in df.columns:
                df['site'] = df['site'].astype(str)
            return df

        finally:
            # Clean up temporary file
            if temp_pdb_path.exists():
                temp_pdb_path.unlink()

    def _calculate_phi_psi(self, pdb_path):
        """Calculate phi/psi backbone angles once from original structure.

        Returns DataFrame with columns: chain_id, residue_number, insertion_code, site, phi, psi
        """
        parser = PDBParser(PERMISSIVE=True, QUIET=True)
        structure = parser.get_structure("pdb", pdb_path)

        # Get PDB ID for metadata
        pdb_id = Path(pdb_path).stem
        chain_info = {}
        if self.metadata_extractor:
            chain_info = self.metadata_extractor.get_chain_info(pdb_id)

        # Only calculate for antibody chains (heavy and light)
        heavy_chain = chain_info.get("heavy_chain", "")
        light_chain = chain_info.get("light_chain", "")
        target_chains = [c for c in [heavy_chain, light_chain] if c]

        if not target_chains:
            return pd.DataFrame()

        # Calculate phi/psi angles using PPBuilder
        ppb = PPBuilder()
        peptides = ppb.build_peptides(structure[0])

        phi_psi_data = []
        for peptide in peptides:
            phi_psi_list = peptide.get_phi_psi_list()
            for residue, (phi, psi) in zip(peptide, phi_psi_list):
                chain_id = residue.get_parent().id

                # Only process antibody chains
                if chain_id not in target_chains:
                    continue

                res_id = residue.id
                residue_num = res_id[1]
                insertion_code = res_id[2].strip()

                # Convert angles to degrees
                phi_deg = np.degrees(phi) if phi is not None else np.nan
                psi_deg = np.degrees(psi) if psi is not None else np.nan

                # Create site label
                site = self._format_site_label(residue_num, insertion_code)

                phi_psi_data.append({
                    "chain_id": chain_id,
                    "residue_number": residue_num,
                    "insertion_code": insertion_code,
                    "site": site,
                    "phi": phi_deg,
                    "psi": psi_deg,
                })

        df = pd.DataFrame(phi_psi_data)
        # Ensure site column is always string type
        if not df.empty and 'site' in df.columns:
            df['site'] = df['site'].astype(str)
        return df

    def calculate_multi_scenario_sasa(self, pdb_path):
        """Calculate SASA for all six scenarios and compare."""

        # Calculate phi/psi angles once from original structure
        phi_psi_df = self._calculate_phi_psi(pdb_path)

        scenarios = [
            "heavy_light_antigen",
            "heavy_light",
            "heavy_only",
        ] + list(HEAVY_REMOVAL_SCENARIOS.keys())
        scenario_results = {}

        # Calculate SASA for each scenario
        for scenario in scenarios:
            try:
                results = self._calculate_sasa(pdb_path, scenario=scenario)
                if not results.empty:
                    scenario_results[scenario] = results
            except Exception as e:
                # Continue with other scenarios if one fails
                print(f"Warning: Failed to calculate SASA for scenario {scenario}: {e}")
                continue

        if not scenario_results:
            return pd.DataFrame()

        # Merge all scenarios
        metadata_cols = [
            "pdb_id",
            "organism",
            "v_gene_light",
            "j_gene_light",
            "v_gene_heavy",
            "j_gene_heavy",
            "heavy_chain_id",
            "light_chain_id",
            "antigen_chains",
        ]
        base_cols = [
            "chain_id",
            "residue_number",
            "insertion_code",
            "site",
            "amino_acid",
            "ca_coordinates",
        ]

        # Start with first available scenario
        first_scenario = list(scenario_results.keys())[0]
        comparison = scenario_results[first_scenario][
            base_cols + metadata_cols + ["sasa", "rsa"]
        ].copy()
        comparison = comparison.rename(
            columns={
                "sasa": f"sasa_{first_scenario}",
                "rsa": f"rsa_{first_scenario}",
            }
        )

        # Merge other scenarios
        for scenario in list(scenario_results.keys())[1:]:
            scenario_data = scenario_results[scenario][
                ["chain_id", "residue_number", "insertion_code", "site", "sasa", "rsa"]
            ]
            scenario_data = scenario_data.rename(
                columns={
                    "sasa": f"sasa_{scenario}",
                    "rsa": f"rsa_{scenario}",
                }
            )
            comparison = pd.merge(
                comparison,
                scenario_data,
                on=["chain_id", "residue_number", "insertion_code", "site"],
                how="outer",
            )

        # Merge phi/psi angles (calculated once from original structure)
        if not phi_psi_df.empty:
            comparison = pd.merge(
                comparison,
                phi_psi_df,
                on=["chain_id", "residue_number", "insertion_code", "site"],
                how="left",
            )

        # Calculate differences and relative changes
        if (
            "sasa_heavy_light_antigen" in comparison.columns
            and "sasa_heavy_light" in comparison.columns
        ):
            comparison["sasa_antigen_effect"] = (
                comparison["sasa_heavy_light_antigen"] - comparison["sasa_heavy_light"]
            )
            comparison["rsa_antigen_effect"] = (
                comparison["rsa_heavy_light_antigen"] - comparison["rsa_heavy_light"]
            )

            comparison["sasa_antigen_relative"] = (
                comparison["sasa_antigen_effect"] / comparison["sasa_heavy_light"]
            ).replace([np.inf, -np.inf], np.nan)

            comparison["rsa_antigen_relative"] = (
                comparison["rsa_antigen_effect"] / comparison["rsa_heavy_light"]
            ).replace([np.inf, -np.inf], np.nan)

        if (
            "sasa_heavy_light" in comparison.columns
            and "sasa_heavy_only" in comparison.columns
        ):
            comparison["sasa_light_effect"] = (
                comparison["sasa_heavy_light"] - comparison["sasa_heavy_only"]
            )
            comparison["rsa_light_effect"] = (
                comparison["rsa_heavy_light"] - comparison["rsa_heavy_only"]
            )

            comparison["sasa_light_relative"] = (
                comparison["sasa_light_effect"] / comparison["sasa_heavy_only"]
            ).replace([np.inf, -np.inf], np.nan)

            comparison["rsa_light_relative"] = (
                comparison["rsa_light_effect"] / comparison["rsa_heavy_only"]
            ).replace([np.inf, -np.inf], np.nan)

        # Calculate removal effects for each heavy chain removal scenario
        for scenario_name in HEAVY_REMOVAL_SCENARIOS:
            sasa_col = f"sasa_{scenario_name}"
            rsa_col = f"rsa_{scenario_name}"
            if (
                "sasa_heavy_only" in comparison.columns
                and sasa_col in comparison.columns
            ):
                effect_tag = scenario_name.replace("heavy_no_", "")
                comparison[f"sasa_{effect_tag}_effect"] = (
                    comparison["sasa_heavy_only"] - comparison[sasa_col]
                )
                comparison[f"rsa_{effect_tag}_effect"] = (
                    comparison["rsa_heavy_only"] - comparison[rsa_col]
                )
                comparison[f"sasa_{effect_tag}_relative"] = (
                    comparison[f"sasa_{effect_tag}_effect"] / comparison[sasa_col]
                ).replace([np.inf, -np.inf], np.nan)
                comparison[f"rsa_{effect_tag}_relative"] = (
                    comparison[f"rsa_{effect_tag}_effect"] / comparison[rsa_col]
                ).replace([np.inf, -np.inf], np.nan)

        return comparison

    def cleanup(self):
        """Remove temporary directory."""
        import shutil

        if self.temp_dir.exists():
            shutil.rmtree(self.temp_dir)


def process_pdb_directory(
    pdb_directory,
    output_file=None,
    max_files=None,
    metadata_extractor=None,
    organism_filter=None,
    verbose=False,
    numbering_scheme=None,
):
    """
    Process all PDB files in a directory with simplified unified filtering pipeline.

    Parameters:
    -----------
    pdb_directory : str or Path
        Directory containing PDB files
    output_file : str or None
        Output CSV file path or None to return DataFrame only
    max_files : int or None
        Maximum number of files to process (for testing)
    metadata_extractor : ProteinMetadataExtractor or None
        Metadata extractor instance, or None to create one
    organism_filter : str or None
        Filter to process only structures from specific organism
    verbose : bool
        Whether to print verbose output
    numbering_scheme : str or None
        Numbering scheme used (e.g., "opig-imgt", "opig-chothia", "rcsb")
        Used to format site labels correctly

    Returns:
    --------
    pd.DataFrame
        Combined results for all PDB files with metadata
    """
    # Initialize metadata extractor (always enabled)
    if metadata_extractor is None:
        metadata_extractor = ProteinMetadataExtractor(verbose=verbose)

    # Apply unified filtering pipeline
    filter_pipeline = StructureFilter(metadata_extractor, verbose=verbose)
    pdb_files = filter_pipeline.apply_filters(pdb_directory, organism_filter, max_files)

    if not pdb_files:
        if organism_filter:
            print(
                f"No PDB files found matching organism '{organism_filter}' in {pdb_directory}"
            )
        else:
            print(f"No valid PDB files found in {pdb_directory}")
        return pd.DataFrame()

    calculator = SASACalculator(metadata_extractor=metadata_extractor, numbering_scheme=numbering_scheme)
    all_results = []
    failed_files = []

    print(f"Processing {len(pdb_files)} validated PDB files...")

    for pdb_file in tqdm(pdb_files, desc="Processing PDBs"):
        try:
            results = calculator.calculate_multi_scenario_sasa(str(pdb_file))
            if not results.empty:
                all_results.append(results)
            else:
                failed_files.append(pdb_file.name)

        except Exception as e:
            if verbose:
                print(f"Error processing {pdb_file.name}: {str(e)}")
            failed_files.append(pdb_file.name)
            continue

    # Cleanup temporary files
    try:
        calculator.cleanup()
    except Exception as cleanup_error:
        if verbose:
            print(f"Warning: Cleanup failed: {cleanup_error}")

    if not all_results:
        print("No files were successfully processed")
        return pd.DataFrame()

    # Combine all results
    combined_results = pd.concat(all_results, ignore_index=True)

    # Save intermediate results to prevent total data loss
    if output_file and len(all_results) > 0:
        temp_output = f"{output_file}.partial"
        try:
            combined_results.to_csv(temp_output, index=False)
            if verbose:
                print(f"Intermediate results saved to {temp_output}")
        except Exception as save_error:
            if verbose:
                print(f"Warning: Could not save intermediate results: {save_error}")

    # Reorder columns for better readability
    # Build removal scenario columns dynamically
    removal_sasa_cols = []
    removal_effect_cols = []
    for scenario_name in HEAVY_REMOVAL_SCENARIOS:
        removal_sasa_cols.extend([
            f"sasa_{scenario_name}",
            f"rsa_{scenario_name}",
        ])
        effect_tag = scenario_name.replace("heavy_no_", "")
        removal_effect_cols.extend([
            f"sasa_{effect_tag}_effect",
            f"rsa_{effect_tag}_effect",
            f"sasa_{effect_tag}_relative",
            f"rsa_{effect_tag}_relative",
        ])

    column_order = [
        "pdb_id",
        "organism",
        "v_gene_light",
        "j_gene_light",
        "v_gene_heavy",
        "j_gene_heavy",
        "heavy_chain_id",
        "light_chain_id",
        "antigen_chains",
        "chain_id",
        "residue_number",
        "insertion_code",
        "site",
        "amino_acid",
        # Backbone angles (same across all scenarios)
        "phi",
        "psi",
        # Raw SASA values
        "sasa_heavy_light_antigen",
        "sasa_heavy_light",
        "sasa_heavy_only",
        "rsa_heavy_light_antigen",
        "rsa_heavy_light",
        "rsa_heavy_only",
        # Removal scenario raw values
        *removal_sasa_cols,
        # Effect calculations
        "sasa_antigen_effect",
        "rsa_antigen_effect",
        "sasa_antigen_relative",
        "rsa_antigen_relative",
        "sasa_light_effect",
        "rsa_light_effect",
        "sasa_light_relative",
        "rsa_light_relative",
        *removal_effect_cols,
        "ca_coordinates",
    ]

    # Only include columns that exist in the DataFrame
    available_columns = [col for col in column_order if col in combined_results.columns]
    combined_results = combined_results.reindex(columns=available_columns)

    # Print summary
    print(f"\n=== Processing Results ===")
    print(f"Successfully processed: {len(all_results)} files")
    print(f"Failed to process: {len(failed_files)} files")
    print(f"Total residues analyzed: {len(combined_results)}")
    print(f"Unique PDB structures: {combined_results['pdb_id'].nunique()}")

    if "v_gene_heavy" in combined_results.columns:
        v_heavy_count = combined_results["v_gene_heavy"].notna().sum()
        j_heavy_count = combined_results["j_gene_heavy"].notna().sum()
        print(f"Residues with heavy chain V gene data: {v_heavy_count}")
        print(f"Residues with heavy chain J gene data: {j_heavy_count}")
    if "v_gene_light" in combined_results.columns:
        v_light_count = combined_results["v_gene_light"].notna().sum()
        j_light_count = combined_results["j_gene_light"].notna().sum()
        print(f"Residues with light chain V gene data: {v_light_count}")
        print(f"Residues with light chain J gene data: {j_light_count}")
    if "organism" in combined_results.columns:
        print(f"Unique organisms: {combined_results['organism'].nunique()}")

    # Save to file if requested
    if output_file:
        combined_results.to_csv(output_file, index=False)
        print(f"Results saved to {output_file}")

    if verbose and failed_files:
        print(
            f"\nFailed files: {failed_files[:10]}{'...' if len(failed_files) > 10 else ''}"
        )

    return combined_results


def validate_output_file(output_path):
    """Test if the output file can be created and written to."""
    try:
        # Check if parent directory exists and is writable
        output_file = Path(output_path)
        parent_dir = output_file.parent

        if not parent_dir.exists():
            print(f"Error: Output directory does not exist: {parent_dir}")
            return False

        if not os.access(parent_dir, os.W_OK):
            print(f"Error: No write permission for directory: {parent_dir}")
            return False

        # Test writing to the file
        test_content = "pdb_id,test\ntest_pdb,123\n"
        with open(output_path, "w") as f:
            f.write(test_content)

        # Clean up test file
        os.remove(output_path)

        print(f"Output file validation successful: {output_path}")
        return True

    except Exception as e:
        print(f"Error: Cannot write to output file {output_path}: {e}")
        return False


